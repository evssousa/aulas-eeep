/** Variáveis
 * Servem para armazenar os dados para podermos usá-los no nosso código.
 * 
 * Criando variáveis:
 * - var: declara uma variável globalmente
 * - let: mais moderno que o var, ele declara uma variáveis localmente.
 * - const: declara uma variável que não pode mudar de valor. Constante e imutável.
 * 
 * Nomeando variáveis:
 * - Nomes significativos.
 * - Cases: kemalCase, PascalCase e snake_case.
 */