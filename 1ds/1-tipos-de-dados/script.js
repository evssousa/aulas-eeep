/** Tipos de dados no Javascript (Tipos primitivos)
 * No JS (e em outras linguagens de programação) dados podem ser representados em diferentes formas:
 * 
 * O que e quais são os tipos primitivos?
 * - number (tanto para int quanto para float) e bigint (para números muito grande, colocamos o n no final para que não dê erro)
 * 
 * - string: texto só é reconhecido com aspas. 
 *  -- Podem ser tratadas como uma lista de caracteres, adicionando [] no final do texto.
 *  -- Podemos também utilizar o .length para ver o tamanho da lista de caracteres.
 * 
 * - boolean: são duas palavras reservadas, true ou false
 * 
 * - null e undefined: null é um valor nulo, um valor vazio (mas tem um valor). Já o undefined, mas este significa 'não definido', não tem valor.
 */
